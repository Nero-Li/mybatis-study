public class Calculator {

    public int add(int x, int y) {
        return x + y;
    }

    public int subtract(int x, int y) {
        return x - y;
    }

    public int mutiply(int x, int y) {
        return x * y;
    }

    public float devide(int x, int y) {
        return x / y;
//        return x * 1f / y;
    }
}
